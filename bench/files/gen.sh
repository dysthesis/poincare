#!/usr/bin/env bash
# Deterministic fixture generator for the benchmark harness (P4).
#
# Generates, next to itself:
#   big.{rs,nix,lua,py,lean}  — >= 2 kLoC each, for M2 (startup + representative
#                               file) and the open-rust/write scenarios.
#   small.{c,go,just,lua,md,nix,py,rs,zig,lean} + justfile
#                             — tiny valid files, one per shipped grammar,
#                               for the M5 treesitter parse probe.
#   lua-project/              — minimal lua-language-server project (one
#                               guaranteed diagnostic) for the M4 probe.
#
# Output is a pure function of this script: identical bytes on every run, so
# both arms of any A/B see the same fixtures. Generated files are gitignored;
# every bench script regenerates them on demand.
set -euo pipefail

dir="$(cd "$(dirname "$0")" && pwd)"
lines=2100 # > 2 kLoC for every big fixture

# --- big fixtures (one short, valid block per iteration) -------------------

{
    echo '// generated by bench/files/gen.sh — do not edit'
    for ((i = 1; i <= lines / 5; i++)); do
        printf 'pub fn f_%d(x: u64) -> u64 {\n' "$i"
        printf '    let y = x.wrapping_mul(%d).wrapping_add(%d);\n' "$i" "$((i % 97))"
        printf '    y ^ (y >> 7)\n'
        printf '}\n\n'
    done
} >"$dir/big.rs"

{
    echo '# generated by bench/files/gen.sh — do not edit'
    echo '{'
    for ((i = 1; i <= lines - 2; i++)); do
        printf '  f_%d = x: let y = x * %d + 1; in y + %d;\n' "$i" "$i" "$((i % 97))"
    done
    echo '}'
} >"$dir/big.nix"

{
    echo '-- generated by bench/files/gen.sh — do not edit'
    echo '-- luacheck: ignore'
    echo '-- selene: allow(unused_variable)'
    for ((i = 1; i <= lines / 5; i++)); do
        printf 'local function f_%d(x)\n' "$i"
        printf '  local y = x * %d + 1\n' "$i"
        printf '  return y %% 97\n'
        printf 'end\n\n'
    done
    echo 'return true'
} >"$dir/big.lua"

{
    echo '# generated by bench/files/gen.sh — do not edit'
    for ((i = 1; i <= lines / 4; i++)); do
        printf 'def f_%d(x: int) -> int:\n' "$i"
        printf '    y = x * %d + 1\n' "$i"
        printf '    return y %% 97\n\n'
    done
} >"$dir/big.py"

{
    echo '-- generated by bench/files/gen.sh — do not edit'
    for ((i = 1; i <= lines - 1; i++)); do
        printf 'def f%d (x : Nat) : Nat := x * %d + 1\n' "$i" "$i"
    done
} >"$dir/big.lean"

# --- small fixtures, one per shipped grammar (M5 parse probe) --------------

cat >"$dir/small.c" <<'EOF'
#include <stdint.h>
uint64_t mix(uint64_t x) {
  uint64_t y = x * 31u + 7u;
  return y ^ (y >> 13);
}
EOF

cat >"$dir/small.go" <<'EOF'
package main

func mix(x uint64) uint64 {
	y := x*31 + 7
	return y ^ (y >> 13)
}
EOF

cat >"$dir/small.zig" <<'EOF'
const std = @import("std");
pub fn mix(x: u64) u64 {
    const y = x *% 31 +% 7;
    return y ^ (y >> 13);
}
EOF

cat >"$dir/small.lua" <<'EOF'
-- luacheck: ignore
local function mix(x)
  local y = x * 31 + 7
  return y % 8191
end
return mix
EOF

cat >"$dir/small.nix" <<'EOF'
{
  mix = x: let y = x * 31 + 7; in y - (y / 13);
}
EOF

cat >"$dir/small.py" <<'EOF'
def mix(x: int) -> int:
    y = x * 31 + 7
    return y % 8191
EOF

cat >"$dir/small.rs" <<'EOF'
pub fn mix(x: u64) -> u64 {
    let y = x.wrapping_mul(31).wrapping_add(7);
    y ^ (y >> 13)
}
EOF

cat >"$dir/small.md" <<'EOF'
# Fixture

Some *emphasis*, a [link](https://example.org), and code:

```lua
return 42
```

- one
- two
EOF

cat >"$dir/small.lean" <<'EOF'
def mix (x : Nat) : Nat := x * 31 + 7

def twice (x : Nat) : Nat := mix (mix x)
EOF

cat >"$dir/justfile" <<'EOF'
default:
    echo hello

build:
    echo build
EOF

# --- lua-language-server project (M4 probe) ---------------------------------
# `unused` is deliberate: lua-language-server publishes an unused-local
# diagnostic for it, which is what the LspAttach -> DiagnosticChanged probe
# waits on.

mkdir -p "$dir/lua-project"

cat >"$dir/lua-project/.luarc.json" <<'EOF'
{
  "runtime": {
    "version": "LuaJIT"
  }
}
EOF

cat >"$dir/lua-project/main.lua" <<'EOF'
local unused = 1
return 42
EOF

echo "bench fixtures generated in $dir" >&2
